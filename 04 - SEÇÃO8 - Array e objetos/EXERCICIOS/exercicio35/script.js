const calculadora = {
  soma: function(a,b){
    return a + b
  },
  subtrair: function(a,b){
    return a - b
  },
  multiplicar: function(a,b){
    return a * b
  },
  dividir: function(a,b){
    return a / b
  }
}
console.log(calculadora.soma(3,4))
console.log(calculadora.subtrair(45,20))
console.log(calculadora.multiplicar(9,9))
console.log(calculadora.dividir(28,2))